let currentLyricIndex = 0;
const audio = document.getElementById("audio");
const freddieLyrics = document.getElementById("lyric-freddie");
const othersLyrics = document.getElementById("lyric-others");
   
audio.addEventListener("play", () => {
    setInterval(displayLyrics, 100);
});
   
function displayLyrics() {
    const currentTime = audio.currentTime;
    
    if (
        currentLyricIndex < lyrics.length &&
        currentTime >= lyrics[currentLyricIndex].time
    ) {
        let lyric = lyrics[currentLyricIndex]
        if(lyric.singer === "freddie"){
            freddieLyrics.innerText = lyric.text;
        }else{
            othersLyrics.innerText = lyric.text;
        }
    currentLyricIndex++;
           
    }
}